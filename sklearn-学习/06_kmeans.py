## cluster.estimate_bandwidth              估计使用均值平移算法的带宽
## cluster.k_means                         K均值聚类
## cluster.mean_shift                      使用平坦核聚类函数平均移位聚类
## cluster.spectral_clustering             将聚类应用于规范化拉普拉斯的投影
